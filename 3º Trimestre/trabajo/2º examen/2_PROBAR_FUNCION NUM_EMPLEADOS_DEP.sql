                   /*DEPARTAMENTO QUE NO EXISTE 123 TIENE  0 EMPLEADOS, NO ROMPE */


SELECT NUMERO, NOMBRE, NUM_EMPLEADOS_DEP(NUMERO) AS CANTIDAD_EMPLEADOS
    FROM DEPARTAMENTOS;
                    /** EN ESTA SELECT, POR CADA TUPLA SELECCIONADA, TODAS LAS DE TABLA DEPARTAMENTOS
                         PINTAMOS COLUMNA NUMERO, NOMBRE Y
                        LANZAMOS UNA VEZ LA FUNCIÓN 
                     **/

SELECT   D.NOMBRE AS DEPARTAMENTO,
         ( 
            SELECT NOMBRE
               FROM EMPLEADOS
               WHERE ID_EMPLEADO=D.DIRECTOR
         )AS SU_DIRECTOR
    FROM DEPARTAMENTOS AS D
    WHERE NUM_EMPLEADOS_DEP(D.NUMERO) >3;

