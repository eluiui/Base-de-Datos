     EL  ARGUMENTO QUE PASAMOS  ES  LA COLUMNA NUMERO DE CADA TUPLA DE TABLA D
     Y COMPARAMOS VALOR QUE RETORNA CON 3, SI ES > 3 LA TUPLA QUEDA SELECCIONADA
**/